    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newPage = new Page();
        $newPage->title = $request->title;
        $newPage->author_id = $request->author_id;
        $newPage->content = $request->content;
        $newPage->excerpt=$request->excerpt;
        $newPage->status= $request->status;
        $newPage->images=$request->images;
        $newPage->public_date = $request->public_date;
        $newPage->created_by=$request->created_by;
        $newPage->updated_by=$request->updated_by;
        $newPage->save();
        return response()->json($newPage);

    }

